void fetchmembers()
{
   
    try
    {
        String url="jdbc:mysql://localhost/eventdb";
        Connection myconn=DriverManager.getConnection(url, "root", "");
        try
        {
            String q="select * from booking";
            
            PreparedStatement state=myconn.prepareStatement(q);
          
            ResultSet res=state.executeQuery();
           while(res.next())
           {
               String mid=res.getString("memberid");
               String bid=res.getString("bookingid");
               String type=res.getString("type");
               String place=res.getString("place");
               String g=res.getString("guests");
               Date date=res.getDate("date");
               String eq=res.getString("equipments");
               String b=res.getString("breakfast");
               String d=res.getString("dinner");
               String t=res.getString("tea");
               String l=res.getString("lunch");
               String li=res.getString("light");
               String f=res.getString("flower");
               String s=res.getString("seating");
               String a=res.getString("amount");
               
               String pm=res.getString("paymode");
               String cn=res.getString("cardno");
               String cv=res.getString("cvvno");
               String can=res.getString("cardname");
               Date ex=res.getDate("expiredate");
               String ap=res.getString("amountpaid");
               String pr=res.getString("pendingrs");
               String ps=res.getString("paymentstatus");
               DefaultTableModel mymodel=(DefaultTableModel)jTable1.getModel();
               Object myrow[]={mid,bid,type,place,g,date,eq,b,d,t,l,li,f,s,a,pm,cn,cv,can,ex,ap,pr,ps};
               mymodel.addRow(myrow);
              
           }
            if(jTable1.getRowCount()==0)
               {
                   JOptionPane.showMessageDialog(rootPane,"no memebers found");
               }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, "error in query"+e.getMessage());
        }
    }
    catch(SQLException e)
    {
       JOptionPane.showMessageDialog(rootPane, "error in connection"+e.getMessage());
    }
}

